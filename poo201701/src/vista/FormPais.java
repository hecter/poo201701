    @Override
    public void limpiarFormulario() {
        textCodigo.setValue(null);
        textNombre.setText(null);
        textIso2.setText(null);
        textIso3.setText(null);
        textNombre.requestFocus();
    }

    @Override
    public void mostrarRegistro(int indice) {
        textCodigo.requestFocus();
        if (indice < 0 || listaDatos.isEmpty()) {
            limpiarFormulario();
            return;
        }
        Pais pais;
        pais = (Pais) listaDatos.get(indice);
        if (pais != null) {
            System.out.println(pais);
            textCodigo.setValue(pais.getCodigo());
            textNombre.setText(pais.getNombre());
            textIso2.setText(pais.getIso2());
            textIso3.setText(pais.getIso3());
            textNombre.requestFocus();
        }
    }

    @Override
    public void inicializar() {
        try {
            listaDatos = new ArrayList<>();
            listaDatos.addAll(Pais.buscar());
            indiceActual = 0;
            habilitarNavegacion();
        } catch (Exception ex) {
            mostrarException(ex);
        }
    }

    @Override
    public SuperTabla getNuevoRegistro() throws Exception {
        Pais pais;
        pais = new Pais(textNombre.getText(), textIso2.getText(),
                textIso3.getText());
        return pais;
    }

    @Override
    public void setRegistroActual(SuperTabla registro) throws Exception {
        Pais pais;
        pais = (Pais) registro;
        pais.setNombre(textNombre.getText());
        pais.setIso2(textIso2.getText());
        pais.setIso3(textIso3.getText());
    }

    @Override
    public void ejecutarBusqueda() throws Exception {
        long codigo = 0;
        codigo = textCodigo.getText().isEmpty()
                ? -1
                : Long.parseLong(textCodigo.getText());
        indiceActual = 0;
        listaDatos = new ArrayList<>();
        if (codigo == -1) {
            listaDatos.addAll(Pais.buscar());
        } else {
            listaDatos.add(Pais.buscar(codigo));
        }
        textCodigo.setEditable(false);
    }

    @Override
    public void habilitarBusqueda() {
        textCodigo.setEditable(true);
        textCodigo.requestFocus();
    }

    @Override
    public void imprimirJasper() {
        try {
            listaDatos.get(indiceActual)
                    .abrirReporte(BaseDatosOracle.getInstance().getConexion());

        } catch (Exception ex) {
            mostrarException(ex);
        }

    }

