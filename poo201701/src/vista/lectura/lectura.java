    public void loadTable(){
        limpiarTabla(tablaDatos);
        try {
            //Medidor medidor = new Medidor();
            ArrayList<String> listado;
                String u = txusuario.getText();
                String s = txsector.getText();
                String p = txperiodo.getText();
                listado = Lectura.ListarPorLeer(u, s, p);
                if (!listado.isEmpty()) {
                    listado.stream().forEach((dato) -> {
                        String[] array = dato.split("\\;");
                        DefaultTableModel tb = (DefaultTableModel) tablaDatos.getModel();
                        Vector datos = new Vector();
                        datos.add(array[0]);
                        datos.add(array[1]);
                        datos.add(array[2]);
                        datos.add(array[3]);
                        tb.addRow(datos);
                });
                }        
        } catch (SQLException ex) {
            msn(this, ex.getMessage(), "ERROR", 0);
        }
    }

